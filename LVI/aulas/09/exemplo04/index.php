<?php

# definindo uma variavel $x para armazernar um número inteiro.
$x = 5985;
# var_dump retorna o tipo e valor da variavel.
var_dump($x);
# var_dump retorna se o valor da variavel é inteiro.
var_dump(is_int($x));
# retorna se o valor máximo da variavel inteiro.
var_dump(PHP_INT_MAX);
# retorna se o valor máximo da variavel inteiro.
var_dump(PHP_INT_MIN);
# var_dump retorna se o valor da variavel string é inteiro.
$x = "5985";
var_dump($x);
var_dump(is_int($x));

