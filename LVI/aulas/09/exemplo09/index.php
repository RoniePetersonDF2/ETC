<?php
# utilizando operador igualdade.
var_dump(20 == "20");
# utilizando operador identico (tipo e valor).
var_dump(20 === "20");
# utilizando operador diferença.
var_dump(20 != "20");
# utilizando operador diferença (outra forma).
var_dump(20 <> "20");
# utilizando operador não identico (tipo e valor).
var_dump(20 !== "20");
# utilizando operador maior.
var_dump(20 > 19);
# utilizando operador menor.
var_dump(20 < 19);
# utilizando operador maior ou igual.
var_dump(20 >= 19);
# utilizando operador menor ou igual.
var_dump(20 < 19);
