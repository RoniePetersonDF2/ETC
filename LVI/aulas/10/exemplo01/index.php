<?php
# definindo uma variavel $x e $y para armazernar números.
$x = 40;
$y = 20;

# utilizando operador de soma.
var_dump($x + $y);
# utilizando operador de subtração.
var_dump($x - $y);
# utilizando operador de multiplicação.
var_dump($x * $y);
# utilizando operador de divisão.
var_dump($x / $y);
# utilizando operador de módulo ou resto da divisão.
var_dump(5 % 3);
# utilizando operador de exponenciação.
var_dump(2 ** 4);
